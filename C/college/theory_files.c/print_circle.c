#include <stdio.h>
#define Equation j *j + i *i- a*a
int main()
{
    float a = 30;
    for (int i = a; i >= -(a); i--)
    {
        for (int j = -a; j <= (a); j++)
        {
            Equation <=  0.7*a &&  Equation > -0.7*a ? printf("\\/") : printf("  ");
            
            // Equation <= 0 ? printf("**") : printf("  ");
        }
        printf("\n");
    }
}
