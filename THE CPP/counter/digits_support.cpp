#include <iostream>
using namespace std;
//counts 1
int count1(string data_lines)
{
    int counter = 0;
    for (int i = 0; i < data_lines.length(); i++)
    {

        if (data_lines[i] == '1')
        {
            counter++;
        }
    }
    return counter;
}

